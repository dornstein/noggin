// Generate the JavaScript API reference page.
//
// Drives TypeDoc + typedoc-plugin-markdown via its CLI to render the
// public surface of cli/**/*.d.mts to markdown, then folds those
// fragments into the docs-site template's body. Hand-written regex
// parsing of .d.mts (the previous approach) silently dropped TSDoc
// descriptions in several cases — TypeDoc uses the real TypeScript
// compiler so the output is faithful.
//
// Config lives in `docs/site/typedoc.json` (entry points, output dir,
// plugin choice, formatting). This module only orchestrates the run
// and wraps the resulting markdown in editorial chrome.

import { execFileSync } from 'node:child_process';
import { readFileSync, rmSync, existsSync, readdirSync, statSync } from 'node:fs';
import path from 'node:path';
import url from 'node:url';

import { renderMarkdown } from '../markdown.mjs';

const here = path.dirname(url.fileURLToPath(import.meta.url));
const siteRoot = path.resolve(here, '..');
const typedocOut = path.join(siteRoot, '_typedoc');
const typedocConfig = path.join(siteRoot, 'typedoc.json');
const typedocBin = path.join(siteRoot, 'node_modules', 'typedoc', 'bin', 'typedoc');

// Editorial intro that lives outside TypeDoc — the "what is this
// reference and how do I use it" framing that doesn't belong on any
// individual symbol's page.
const INTRO_MD = `
# JavaScript API

For consumers embedding noggin in a Node process — the VS Code
extension, custom tooling, tests. The reference below is generated by
[TypeDoc](https://typedoc.org) from the hand-written \`.d.mts\` files
under [\`cli/\`](https://github.com/dornstein/noggin/tree/main/cli),
so descriptions, signatures, and release tags stay in sync with the
source automatically.

## Release tiers

Every public symbol carries a TSDoc release tag. Breaking changes
respect the tier:

- **\`@public\`** — stable. Breaking changes require a major bump.
- **\`@experimental\`** — public but the shape may still change.
- **\`@deprecated\`** — still works; scheduled for removal in a future major.

Symbols tagged \`@internal\` exist in the source but are deliberately
hidden from this reference. Consumers should not depend on internal
exports.

## Quick example

\`\`\`js
import { openNoggin, verbs } from 'noggin-cli/noggin-api.mjs';
import 'noggin-cli/providers/file.mjs'; // side-effect: registers file://

const noggin = await openNoggin('/path/to/.noggin.yaml', { watch: true });
const view = await verbs.push(noggin, { title: 'go async' });
console.log(noggin.active?.title);
noggin.onDidChange(() => render(noggin.items));
await noggin.dispose();
\`\`\`

Verbs are pure functions over a \`Noggin\`: they read state via the
noggin's accessors, compose an \`AtomicOp[]\`, and call \`noggin.apply(ops)\`
once. The file provider serializes per-instance calls through an
internal queue; cross-process callers are protected by an advisory
file lock.

`.trimStart();

// Modules in the order we want them rendered. Other typedoc output
// files are ignored (we ignore README.md — it's just an auto-generated
// table of contents and would duplicate the H2s below).
const MODULE_ORDER = [
  { slug: 'noggin-api', label: 'noggin-api' },
  { slug: 'providers/file', label: 'providers/file' },
  { slug: 'serializers/yaml', label: 'serializers/yaml' },
  { slug: 'serializers/json', label: 'serializers/json' },
];

export function buildApiPage() {
  runTypedoc();

  const sections = [INTRO_MD];

  for (const { slug, label } of MODULE_ORDER) {
    const file = path.join(typedocOut, ...slug.split('/')) + '.md';
    if (!existsSync(file)) {
      sections.push(`## ${label}\n\n*(no TypeDoc output for this module)*\n`);
      continue;
    }
    const raw = readFileSync(file, 'utf8');
    sections.push(normalizeModule(raw, label));
  }

  return renderMarkdown(sections.join('\n\n'));
}

// ── Internals ────────────────────────────────────────────────────────────────

function runTypedoc() {
  if (!existsSync(typedocBin)) {
    throw new Error(
      `TypeDoc isn't installed. Run \`npm install\` in docs/site/.\n` +
      `Looked for: ${typedocBin}`,
    );
  }
  // Wipe stale output so deleted symbols don't linger.
  rmSync(typedocOut, { recursive: true, force: true });
  // typedoc CLI is a small JS shim; invoke via node so we don't rely
  // on shell PATH behavior (Windows vs. POSIX).
  execFileSync(process.execPath, [typedocBin, '--options', typedocConfig], {
    cwd: siteRoot,
    stdio: 'inherit',
  });
}

// TypeDoc renders each module with a top-level `# <module>` heading
// followed by per-symbol sections at H2+. We rewrite that to:
//   - `## <module>` as the module heading (under our `# JavaScript API`)
//   - everything else demoted by one level so symbol headings sit under
//     the module rather than competing with it.
function normalizeModule(md, label) {
  const lines = md.split(/\r?\n/);
  const out = [`## ${label}`, ''];
  let inFence = false;
  // Skip the first `# ...` line — we already wrote our own.
  let i = 0;
  while (i < lines.length && lines[i].trim() === '') i++;
  if (i < lines.length && /^#\s/.test(lines[i])) i++;
  while (i < lines.length && lines[i].trim() === '') i++;
  for (; i < lines.length; i++) {
    const line = lines[i];
    // Don't touch heading-looking lines inside fenced code blocks.
    if (/^```/.test(line)) {
      inFence = !inFence;
      out.push(line);
      continue;
    }
    if (!inFence) {
      // Demote every Markdown ATX heading by one level. The renderer caps
      // out at H6, which is fine — even deeply nested method docs land
      // at H5/H6.
      const headingMatch = line.match(/^(#{1,6})\s+(.*)$/);
      if (headingMatch) {
        const newLevel = '#'.repeat(Math.min(6, headingMatch[1].length + 1));
        out.push(`${newLevel} ${headingMatch[2]}`);
        continue;
      }
    }
    out.push(line);
  }
  return out.join('\n');
}
